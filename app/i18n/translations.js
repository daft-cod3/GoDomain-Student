function normalizeText(value) {
  return value.trim().replace(/\s+/g, " ").toLowerCase();
}

function applyCase(source, translated) {
  if (source.toUpperCase() === source) {
    return translated.toUpperCase();
  }

  if (source[0]?.toUpperCase() === source[0]) {
    return translated[0]?.toUpperCase() + translated.slice(1);
  }

  return translated;
}

export function getLanguage(code) {
  return languages.find((language) => language.code === code) ?? languages[0];
}

export function isSupportedLanguage(code) {
  return languageCodes.includes(code);
}

export function translateText(value, languageCode) {
  if (!value || languageCode === "english") {
    return value;
  }

  const phraseMap = phraseTranslations[languageCode] ?? {};
  const wordMap = wordTranslations[languageCode] ?? {};
  const normalized = normalizeText(value);
  const phrase = phraseMap[normalized];

  if (phrase) {
    return value.replace(value.trim(), phrase);
  }

  return value.replace(/[A-Za-z]+(?:'[A-Za-z]+)?/g, (word) => {
    const translated = wordMap[word.toLowerCase()];

    if (!translated) {
      return word;
    }

    return applyCase(word, translated);
  });
}
